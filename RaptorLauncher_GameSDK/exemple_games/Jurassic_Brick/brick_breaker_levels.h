// lecture sécurisée PROGMEM
static inline uint8_t bbGetBrickType(uint8_t level, uint8_t row, uint8_t col) {
  if (level >= BB_LEVEL_COUNT || row >= BB_ROWS || col >= BB_COLS) return BRICK_EMPTY;
  return pgm_read_byte(&LEVEL_MAPS[level][row][col]);
}

static inline uint8_t bbGetLevelBg(uint8_t level) {
  if (level >= BB_LEVEL_COUNT) return BG_FOND_JEUX;
  return pgm_read_byte(&LEVEL_INFO[level].bgType);
}

static inline bool bbLevelHasBonus(uint8_t level) {
  if (level >= BB_LEVEL_COUNT) return false;
  return pgm_read_byte(&LEVEL_INFO[level].hasBonus) != 0;
}